#include "holberton.h"
/**
 * print_triangle - check the code for Holberton School students.
 * @n : check
 * Return: Always 0.
 */
void print_triangle(int size)
{
	int i, j;

	j = 0;
		for (i = 0 ; i < size ; i++)
		{
			if (size > 0)
				for(j = i ; j >= 0 ; j--)
					_putchar (' ');
					_putchar ('#');
			_putchar (10);
	}
		if (size <= 0)
			_putchar (10);

}
